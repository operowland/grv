 <p>Travelling by the countryside, we often enjoy the beautiful scenery of trees of diverse kind basking in their glory. We do enjoy this beauty too in our gardens. However, it is expedient to note that what we see and like is an outward manifestation of a process going on in the soil. <br />
  What excite all is the fruit- the visible manifestation of growth. However, the way to bear fruits is to take roots downward. The roots must reach and go beyond the underlying rock. Christ remains our rock. We must keep digging deeper into Christ.<br />
  Just as natural forces bring out fruits in a tree taking roots downward, God is at work when we commit ourselves to the task of taking root in him by learning of him. God gets to work for, in, on and through us.</p>
<p>What God offers us and makes readily available to us as he beckons on us to learn of his Son is the same life found in his Son. The quality of this life tremendously outweighs the normal life. It is the God kind of life- the Zoë life, which can best be described as God living on this planet. Imagine the wealth. Imagine the strength of character. Imagine the authority. Imagine the responsibility. It is a life that cannot be totally comprehended by human reasoning. Yet he made it available unto us. <br />
  From inception, he wanted this life for us. Hence, he devised a means through Christ to adopt us as sons. Not just sons, but co-heirs with the Son. Since we are co-heirs, we are entitled to inherit what the Son inherits. But we must grow to become sons that can be entrusted with the inheritance. </p>
<p>Learning is what grows and matures us. You can't grow beyond that which you have learnt. Hence, if we will experience continuous growth, then we must continue to learn of him.<br />
  We know more of God by knowing more of his word; becoming diligent in the study of the word. God has written himself in words. The mind of God is the word of God. It tells us the character and personality of God.<br />
  We should not only learn him individually in our closets, we should replicate it in the public. In our busy generation, we must still find means of discussing God's word and not just trending topics amidst ourselves when we go on the streets of our workplaces, classes, markets, parks and social networks. The word of God is always trending! </p>
<p>As we learn of Christ, we inherit more responsibilities in him; responsibilities to God, his church, ourselves and the world at large. Learning of Christ earns us more assignments as the kingdom certifies us to be growing. To whom much is given much is expected. The much of Christ you have possessed is to be used to do much. God now expects much from you as regards your relationship and fellowship with him. <br />
  Your responsibility to yourself also increases, as you must meet and step up your disciplines, challenging yourself to more growth. </p>
<p>In this time we live in, many people are itching to hear falsehood. To them, the truth is bitter and falsehood is sweet. The young Christian has the responsibility to be ready to correct the wrongs and present the truth anywhere the opportunity arises and keep on preaching the truth though many are rejecting it and chasing after myths.<br />
  Age and years do not make you young but your state and day to day experiences do. <br />
  I am writing to you, my dear children, because your sins have been forgiven because of Jesus. I am writing to you who are mature because you know Christ, the one who is from the beginning. I am writing to you who are young because you have won your battle with Satan. I have written to you, children, because you have known the Father. I have written to you who are mature because you know Christ, the one who is from the beginning. I have written to you who are young because you are strong with God's word living in your hearts, and you have won your battle with Satan.<br />
  1 John 2:12-14 NLT</p>
<p>Be that strong believer, with God's word living in your hearts, winning your battle with Satan.<br />
  BE THE YOUNG CHRISTIAN!</p>
    
