 This book examines the dynamics of those ministries that seems to fall outside the normal local church ministries. These ‘outside the church walls’ ministries often occur to us as ideas.<br /> 
God is the idea owner and he inspires us with ideas from time to time. These ideas, which are his and for him, are related with his purpose and intent on earth. They are your portion in the kingdom and your part in expanding the kingdom. God give you ideas that are parts of his bigger idea; the other parts are given to other people. <br /> 
Everyone’s idea is unique. Ideas might look close, but they are still different. There will still be one thing the Lord has willed to differ. There will be a variation even between two close ideas. Your responsibility is to express the idea given to you as the idea manager. <br /> 
From conviction to impact, leveraging to hatching, changes to adversaries, administration to jurisdiction, get equipped to express your ministries by pouring out the measure of Christ in you through your God-given and God-driven ideas.
