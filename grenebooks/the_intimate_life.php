God created us for intimacy. Another word for intimacy is fellowship. The root word is a dear one. That's the word applied in John chapter fifteen as abide, friends. So referring to someone as a darling is saying my intimate one. God has always been interested in fellowship- in intimacy. <br />
He made us in his image. That is we share some characteristics with God. One of the characteristic God has is communication. <br />
Between the Father, the Son and the Holy Spirit there was an intimate relationship, which involves a constant communication. In addition, God wanted that communication in the new creature he was creating. He created us in his image, with common characteristics, which include constant communication. <br />
God came to fellowship with man; there was intimacy. Before now, Adam was productive. The assignment he was given included naming all the animals, which he did. However, his productivity ended when God stopped fellowshipping with him. When God came and discovered their disobedience, and stayed away, that was the end of the story of Adam. The next we heard was he begat this and that and then he lived some number of years. He ended his productivity. <br />
The person that will get much result in life must be intimate with God. Intimacy makes the difference. <br />
Fruits can only be achieved by intimacy. If you do not bear fruit, he will cut you away. You will be as a branch cut away and left to wither off then gathered to be burnt. Withering does not happen suddenly, it is a gradual thing. It means that some branches are already dead and we think they are still alive. They still look green and still seems to have fresh leaves but it has actually started to whither off. Let us just give it time. <br />
Fruitfulness ends when intimacy ends. Some people's intimacy have ended but they are still seeing little results because withering is a process. It is advisable that before your withering shows, you are engrafted back to the vine. <br />
Abide is to remain in him and stay with him. If you do not stay with him, you cannot bear fruit. You need to be intimate so that you can bear fruits, so that there can be the desired results. <br />
The second Adam- Christ -was fruitful because of intimacy. <br />
Jesus made it clear that throughout his ministry that he remained in his father's love. Remain talks about intimacy with the father. <br />
He kept himself intimately in his father's love. He maintained his intimacy with his father. <br />
God is interested in our intimacy. The first Adam missed it but the second Adam did not miss it.<br />
Your intimacy with God is dependent on you. How close you are to God is dependent on you. Intimacy is a personal thing. The closer you go to God, the closer God comes to you. God cannot be mocked, it is what you put in that you get. The intimacy you sense in other peoples relationship with God is a result of their investment. <br />
What you have now is the output of your investment in your relationship with him. You might not see the result of what you invest immediately, just as withering does not show immediately. Your Christian life now is a result of the level of your intimacy with God before now. It is not always immediate. Your experience at times is a function of your spirit picking from your reservoir. However, that reservoir needs to be refilled continuously. <br />
Draw near to God and he will draw near to you. Your productivity is dependent on intimacy and your intimacy depends on how much you are ready to meet him. <br />
This book offer insight to the 'what', 'why', 'who', 'when', 'where' and 'how' questions of intimacy with God.
