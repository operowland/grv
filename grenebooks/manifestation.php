 Manifestation...the man in the manifest carrying out the manifesto<br />
God does not want to stay perpetually in the heavens; he wants to be around here with us. However, God is Spirit so he came through Jesus. Jesus could not remain physically, so he allowed his Spirit to be multiplied in us that he might manifest himself through us in accordance to the plan he has for us as individuals from the foundations of the earth. <br />
Since Christ wants to flow through all, he allows a portion of his measure to be released through individuals. Christ had the measure in full but distributed it in parts to all believers. So Christ is reaching everywhere through us. Hence, our purposes express and extend that of Christ. Jesus has believers involved in every profession and trade that he might keep bruising the serpent's head. So Jesus is still everywhere though some might be denying him expression. <br />
There is the urgent need for believers to live the life of manifesting Christ in their homes, neighborhood, workplace, schools and the likes. <br />
God is the one that gives our respective purpose to us; we do not choose it. You do not choose the measure of Christ that flows down to you. Your particular purpose in the purpose of Christ is your measure. <br />
However, aim to maximize it by becoming like Christ who owns the measure. The best way to do it the way he wants you to do it is to become like him. To become like someone is to think, talk and act like the person. Thinking, talking and acting like the owner will deliver the same result as the owner.<br /> 
