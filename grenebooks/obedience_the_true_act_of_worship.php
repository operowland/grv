 <p>You might have voted for the opposition in the last election and claim the present president is not your president; you are only deceiving yourself. Your choice and preference does not matter again; the president remains the president. Whether you voted for him, his opponents or you abstained, he is still the president.</p>
<p>God has made Christ Lord through the processes of purchase, inheritance and designation.</p>
<p>One&rsquo;s response to this truth could be to reject him or accept his lordship. Whether one accept his lordship or not, it does not change him from being Lord.</p>
<p>So many people have acknowledged the lordship of Christ publicly and privately. So many people say &lsquo;Jesus is Lord&rsquo;, but indeed and in reality, Jesus is not their Lord as their allegiance is not with Christ. To say Jesus is Lord is to pledge allegiance.</p>
<p>The proper response to the Lordship of Christ is to worship him. Worship is beyond attending religious services and gatherings.</p>
<p>In worship, we relate with the Lord from the position of righteousness as we stand in obedience to the gospel. In worship, we serve and surrender all to Christ in obedience. In worship, we continuously express our love and his life through our obedience. In worship, our lives come in union with the Lord, as we obediently trust in him.</p>
<p>The call to worship is a call to obedience.</p>
    
